function normalize(str) { return str.normalize("NFD").replace(/[\u0300-\u036f]/g, ""); }

const words = ["россия", "москва", "санкт-петербург", "питер", "казань", "сочи", "владивосток", "новосибирск", "екатеринбург", "баку", "тбилиси", "киев", "київ", "турция", "стамбул", "анкара", "анталья", "анталия", "измир", "бодрум", "бурса", "каппадокия"];

words.forEach(w => {
    console.log(`${w} -> ${normalize(w)}`);
});
